#include <stdio.h>
#include "ft_abs.h"

int	main(void)
{
	int	values[] = {0, -5, 42, -100, 7};
	int	i;

	i = 0;
	while (i < 5)
	{
		printf("ABS(%d) = %d\n", values[i], abs(values[i]));
		i++;
	}
	return (0);
}
