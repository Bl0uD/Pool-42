void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	c;

	i = 0;
	if (i < size)
	{
		while (tab[i] != '\0' )
		{
			c = tab[i];
			tab[i] = tab[size];
			tab[size] = c;
			i++;
			size--;
		}
	}
}
