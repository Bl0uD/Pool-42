#include "ft.h"

void	ft_support(void *b, unsigned long n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)b;
	while (n > 0)
	{
		n--;
		*(ptr) = 0;
		ptr++;
	}
}
